def _build_text_generic(store: dict) -> str:
    """النص العام (Telegram/Threads/X/FB/Discord) — يحفظ الشكل الأصلي
    مع رابط الـaffiliate الذي يعمل على هذه المنصات."""
    name        = (store.get("store_id") or "").strip()
    name_en     = (store.get("name_en") or "").strip()
    coupon      = (store.get("public_coupon") or "").strip()
    discount    = (store.get("discount_value") or "").strip()
    extra       = (store.get("extra_offer") or "").strip()
    bio         = (store.get("store_bio") or "").strip()
    description = (store.get("description") or "").strip()
    nbthah      = bio or description
    link        = (store.get("affiliate_link") or "").strip()

    name_line = name + (f" ({name_en})" if name_en and name_en.lower() != name.lower() else "")

    lines = [
        "نبض الصفقات",
        "",
        f"المتجر: {name_line}",
    ]
    if discount:
        lines.append(f"نسبة الخصم: {discount}")
    if coupon:
        lines.append(f"كود الخصم: {coupon}")
    if extra:
        lines.append(f"عرض إضافي: {extra}")
    if nbthah:
        lines.append("")
        lines.append(f"نبذة: {nbthah}")
    lines.append("")
    lines.append("استفد من العرض من خلال الرابط التالي:")
    lines.append(link or "—")

    hashtags = _build_hashtags(store, platform="generic")
    if hashtags:
        lines.append("")
        lines.append(" ".join(hashtags))

    return "\n".join(lines)


def _build_text_instagram(store: dict) -> str:
    """نص إنستقرام — مُحسَّن لخوارزمية المنصة:

    - أول سطر: scroll-stopper برقم الخصم + اسم المتجر + كلمة «كوبون» (مفتاحية)
    - السطر الثاني: نداء حفظ (الـsave أقوى إشارة عند الخوارزمية بعد Reels)
    - الكود إن وُجد ضمن البلوك ليُنسخ بسهولة
    - كثافة كلمات: ندمج «كوبون» «كود خصم» «عروض» في الجمل (يفهرسها IG كنص)
    - CTA: «الرابط في البايو» — لأن روابط الـcaption ميتة على إنستقرام
    - الهاشتاقات في القاع (~25 هاشتاق من البنك)"""
    name        = (store.get("store_id") or "").strip()
    name_en     = (store.get("name_en") or "").strip()
    coupon      = (store.get("public_coupon") or "").strip()
    discount    = (store.get("discount_value") or "").strip()
    extra       = (store.get("extra_offer") or "").strip()
    bio         = (store.get("store_bio") or "").strip()
    description = (store.get("description") or "").strip()
    nbthah      = bio or description

    # ── الـHook: أول سطرين هما اللي يبان في الـfeed قبل «عرض المزيد» ──
    # نضع الرقم/الخصم أولاً (scroll-stopper). نوّع الخطّاف لكسر رتابة الفيد —
    # اختيار ثابت لكل متجر (حسب اسمه+خصمه)، فيبقى متّسقاً للمتجر ومتنوّعاً عبر المتاجر.
    def _pick(options: list[str]) -> str:
        seed = f"{name}|{discount}|{extra}"
        return options[sum(ord(c) for c in seed) % len(options)]

    if discount:
        hook = _pick([
            f"كود خصم {name} — {discount} 🔥",
            f"وفّر {discount} على {name} بكود واحد 🔥",
            f"لا تشتري من {name} قبل ما تاخذ كود الخصم 🔥",
            f"{discount} خصم على {name} — الكود تحت 👇",
            f"خصم {discount} ينتظرك في {name} 🔥",
        ])
    elif extra:
        hook = _pick([
            f"عرض حصري من {name} — {extra} 🔥",
            f"{extra} في {name} — لا يفوتك 🔥",
            f"لا تشتري من {name} قبل ما تشوف هذا العرض 🔥",
        ])
    else:
        hook = _pick([
            f"كوبون {name} — وفّر أكثر على مشترياتك 🔥",
            f"لا تشتري من {name} بدون كود الخصم 🔥",
            f"أحدث كود خصم {name} — جرّبه الحين 🔥",
        ])

    lines: list[str] = [
        hook,
        "احفظ المنشور 📌 — نضيف كوبونات وأكواد خصم يومياً",
        "",
    ]

    # ── البلوك التفصيلي (يظهر بعد ضغطة «عرض المزيد») ──
    lines.append(f"🏪 المتجر: {name}" + (f" / {name_en}" if name_en and name_en.lower() != name.lower() else ""))
    if discount:
        lines.append(f"💸 نسبة الخصم: {discount}")
    if coupon:
        lines.append(f"🎟️ كود الخصم: {coupon}")
        lines.append("    👆 انسخ الكود واستخدمه عند الدفع")
    if extra:
        lines.append(f"🎁 عرض إضافي: {extra}")
    if nbthah:
        lines.append("")
        lines.append(f"📝 {nbthah}")

    # ── CTA: «الرابط في البايو» — هذا يفك القفل عن التحويل على إنستقرام ──
    lines.append("")
    lines.append("🔗 الرابط في البايو — اضغطه واستخدم الكود قبل ما ينتهي")
    lines.append("💬 علّق بـ⭐ لو الكود اشتغل معك — ساعد غيرك")
    lines.append("➕ تابعنا لأحدث كوبونات وعروض السعودية يومياً")

    # ── الهاشتاقات ──
    hashtags = _build_hashtags(store, platform="instagram")
    if hashtags:
        lines.append("")
        lines.append(".")  # فاصل صامت يُبعد كتلة الهاشتاقات بصرياً
        lines.append(".")
        lines.append(" ".join(hashtags))

    return "\n".join(lines)


def build_post_text(store: dict, platform: str = "generic") -> str:
    """
    يبني نص المنشور.

    Args:
        store: صف master كقاموس (store_id, name_en, public_coupon, ...).
        platform: 'instagram' للنسخة المُحسَّنة لخوارزمية إنستقرام،
                  أو أي قيمة أخرى للنسخة العامة (Telegram/Threads/X/FB).

    المتوقّع في `store`:
      store_id, name_en, public_coupon, discount_value, extra_offer,
      description, store_bio, store_tags, affiliate_link
    الحقول الاختيارية تُحذف من النص لو ناقصة.
    """
    if platform == "instagram":
        return _build_text_instagram(store)
    return _build_text_generic(store)
