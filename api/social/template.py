def _hashtagify(value: str) -> str | None:
    """يحوّل نصاً إلى هشتاق واحد نظيف:
    يحذف الرموز/علامات الترقيم، ويستبدل الفراغات بشرطة سفلية.
    يبقي الحروف العربية واللاتينية والأرقام. يُرجع None لو خرج فارغاً."""
    if not value:
        return None
    text = value.strip().lstrip("#")
    text = re.sub(r"[^\w\s]", "", text, flags=re.UNICODE)
    text = re.sub(r"\s+", "_", text.strip(), flags=re.UNICODE).strip("_")
    return f"#{text}" if text else None


def _parse_store_tags(raw: str | None) -> list[str]:
    """store_tags نصّ بصيغة array-literal '{a,b,c}'. نفكّكه إلى قائمة نصوص."""
    if not raw:
        return []
    inner = raw.strip().strip("{}")
    return [t.strip().strip('"') for t in inner.split(",") if t.strip()]


def _normalize_tag(tag: str) -> str:
    """يُطبّع نص التاق قبل البحث في القاموس.

    اكتُشف في DB أن master.store_tags يحتوي همزات وألفات مختلطة لنفس الكلمة:
    «ازياء» و«أزياء» مفصولتان كمفتاحين في بايثون رغم أنهما نفس المعنى.
    نطبّع: همزة الألف (أ إ آ ٱ) → ا، الياء المقصورة (ى) → ي، التاء المربوطة (ة) → ه،
    ثم نُسقط التشكيل والفراغات الزائدة ونحوّل للحروف الصغيرة (للاتيني)."""
    if not tag:
        return ""
    s = tag.strip().lower()
    # توحيد الهمزة على الألف
    for ch in ("أ", "إ", "آ", "ٱ"):
        s = s.replace(ch, "ا")
    # توحيد الياء والتاء المربوطة
    s = s.replace("ى", "ي").replace("ة", "ه")
    # إسقاط التشكيل (fatha, kasra, damma, shadda, sukun, tanween)
    s = re.sub(r"[ً-ْ]", "", s)
    # ضغط الفراغات الداخلية
    s = re.sub(r"\s+", " ", s)
    return s


# نُطبّع كل مفاتيح القاموس مسبقاً (دفعة واحدة عند الاستيراد). أي tag جديد
# يأتي من DB يُطبَّع بنفس الدالة، ثم نبحث في الخريطة المُطبَّعة. هذا يضمن
# أن «ازياء» و«أزياء» يجدان نفس مجموعة الهاشتاقات بدون تكرار في القاموس.
_CATEGORY_KEYWORDS_NORMALIZED: dict[str, list[str]] = {
    _normalize_tag(k): v for k, v in CATEGORY_KEYWORDS.items()
}


def _brand_variations(store_name: str) -> list[str]:
    """يولّد هاشتاقات حول اسم المتجر تستهدف باحث الكود/الخصم/العروض لهذا المتجر."""
    base = _hashtagify(store_name)
    if not base:
        return []
    # نضرب على أنماط البحث الشائعة في السعودية (كوبون_X، كود_X، خصم_X، عروض_X).
    raw = base.lstrip("#")
    return [
        base,
        f"#كوبون_{raw}",
        f"#كود_{raw}",
        f"#خصم_{raw}",
        f"#عروض_{raw}",
    ]


def _dedupe_keep_order(items: list[str]) -> list[str]:
    seen: set[str] = set()
    out: list[str] = []
    for it in items:
        if not it:
            continue
        k = it.lower()
        if k not in seen:
            seen.add(k)
            out.append(it)
    return out


